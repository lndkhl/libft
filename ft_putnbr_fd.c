#include "libft.h"

void			ft_putnbr_fd(int n, int fd)
{
	char		c;

	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	if (n < 0)
	{
		long	i;
		i = (long)n;
		write (fd, "-", 1);
		i *= -1;
		if (i == 2147483648)
		{
			write (fd, "2", 1);
			i = 147483648;
		}
		ft_putnbr_fd(i, fd);
	}
	c = n % 10 + '0';
	write (fd, &c, 1);
}
