#include "libft.h"

char		*ft_strdup(const char *s)
{
	int		i;
	char	*d;

	i = 0;

	while (s[i] != '\0')
		i++;
	d = (char *)malloc(sizeof(char) * (i + 1));
	d[i--] = '\0';
	while (i >= 0)
		d[i] = s[i--];
	return (d);
}
