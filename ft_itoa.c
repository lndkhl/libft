#include "libft.h"

char		*ft_itoa(int i)
{
	char	*r;
	int		c;
	int		s;

	r = (char *)malloc(sizeof(char) * 12);
	c = 0;
	s = 1;
	if (i < 0)
	{
		s = -1;
		i *= -1;
	}
	while (c < 12)
		r[c++] = '\0';
	c = 11;
	while (i > 0)
	{
		r[c--] = i % 10;
		i /= 10;
	}
	if (s < 0)
		r[i] = '-';
	while (c < 12)
		r[i++] = r[c++];
	r[i] = '\0';
	return (r);
}
