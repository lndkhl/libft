#include "libft.h"

char					*ft_itoa(int i)
{
	char				*r;
	int					c;
	int					a;

	c = 0;
	a = i;
	while (a != 0)
	{
		a /= 10;
		c++;
	}
	r = (i < 0) ? ft_strnew(c) : ft_strnew(++c);
	if (!r)
		return (NULL);
	if (i < 0)
		r[0] = '-';
	while (i != 0)
	{
		r[--c] = (a % 10) + '0';
		a /= 10;
	}
	return (r);
}
