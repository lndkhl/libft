#include "libft.h"

int				ft_atoi(const char *s)
{
	long		n;
	long int	i;
	long int	r;

	i = 0;
	n = 1;
	r = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n' || s[i] == '\r' || s[i] == '\f')
		i++;
	if (s[i] == '-')
	{
		n = -1;
		i++;
	}
	else if (s[i] == '+')
		i++;
	while (s[i] > 47 && s[i] < 58)
	{
		r = (r * 10) + (s[i] - 48);
		i++;
	}
	return (r * n);
}
