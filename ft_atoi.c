#include "libft.h"

int				ft_atoi(const char *s)
{
	int			n;
	int			i;
	long long	r;

	i = 0;
	n = 1;
	r = 0;
	while (s[i] == 32 || s[i] == '\t' || s[i] == '\n' || s[i] == '\r' || s[i] == '\f' || s[i] == '\v')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			n = -1;
		i++;
	}
	while (ft_isdigit(s[i]))
	{	
		r = (r * 10) + (s[i] - 48);
		if (r > 2147483648 && n == -1)
			return (0);
		else if (r > 2147483647 && n == 1)
			return (-1);
		i++;
	}
	return (r * n);
}
